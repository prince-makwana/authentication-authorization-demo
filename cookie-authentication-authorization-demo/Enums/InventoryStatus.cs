/// <summary>
/// Enum representing the possible statuses of inventory
/// </summary>
public enum InventoryStatus
{
    /// <summary>
    /// Inventory is in stock
    /// </summary>
    InStock,

    /// <summary>
    /// Inventory is low
    /// </summary>
    LowStock,

    /// <summary>
    /// Inventory is out of stock
    /// </summary>
    OutOfStock,

    /// <summary>
    /// Inventory is on order
    /// </summary>
    OnOrder
} 